let grade = 30;

if (grade >= 80) {
  console.log("Congratulations! You have an A");
} else if (grade >= 70) {
  console.log("Good job! You have a B");
} else if (grade >= 60) {
  console.log("Keep it up! You have a C");
} else if (grade >= 50) {
  console.log("Try a little harder next time! You have a D");
} else {
  console.log("Oh No! You have an F");
}
